class ResidualPolicyNetwork(nn.Module):
    """
    Residual policy network for PPO
    
    Outputs residual corrections to LQR baseline:
    u_total = u_LQR + π_residual(state)
    """
    
    def __init__(self, config: NetworkConfig):
        super().__init__()
        self.config = config
        
        # State encoder (larger than evolution network)
        self.encoder = nn.Sequential(
            nn.Linear(config.input_dim, 64),
            nn.Tanh(),
            nn.Linear(64, 64),
            nn.Tanh(),
            nn.Linear(64, 32),
            nn.Tanh()
        )
        
        # Policy head (mean of action distribution)
        self.policy_mean = nn.Sequential(
            nn.Linear(32, 16),
            nn.Tanh(),
            nn.Linear(16, config.output_dim),
            nn.Tanh()  # Bounded output
        )
        
        # Policy log std (learnable parameter)
        self.log_std = nn.Parameter(torch.zeros(config.output_dim))
        
        self._initialize_weights()
        
    def _initialize_weights(self):
        """Initialize weights for stable training"""
        def _init_linear(m: nn.Module) -> None:
            if isinstance(m, nn.Linear):
                nn.init.orthogonal_(m.weight)
                nn.init.zeros_(m.bias)

        self.encoder.apply(_init_linear)
        self.policy_mean.apply(_init_linear)
                    
        # Small initial log_std for exploration
        nn.init.constant_(self.log_std, -1.0)
    
    def forward(self, state: torch.Tensor) -> Tuple[torch.Tensor, torch.Tensor]:
        """
        Forward pass for residual policy
        
        Args:
            state: State tensor [batch_size, input_dim]
            
        Returns:
            (mean, std) of action distribution
        """
        features = self.encoder(state)
        mean = self.policy_mean(features) * self.config.output_scale
        std = torch.exp(self.log_std).expand_as(mean)
        
        return mean, std


class ValueNetwork(nn.Module):
    """Value function network for PPO critic"""
    
    def __init__(self, config: NetworkConfig):
        super().__init__()
        self.config = config
        
        self.network = nn.Sequential(
            nn.Linear(config.input_dim, 64),
            nn.Tanh(),
            nn.Linear(64, 64),
            nn.Tanh(),
            nn.Linear(64, 32),
            nn.Tanh(),
            nn.Linear(32, 1)  # Single value output
        )
        
        self._initialize_weights()
        
    def _initialize_weights(self):
        """Initialize weights for stable value learning"""
        def _init_linear(m: nn.Module) -> None:
            if isinstance(m, nn.Linear):
                nn.init.orthogonal_(m.weight)
                nn.init.zeros_(m.bias)
        
        self.network.apply(_init_linear)
        
        # Small final layer for stable value estimates
        final_layer = cast(nn.Linear, self.network[-1])
        nn.init.orthogonal_(final_layer.weight, gain=0.1)
    
    def forward(self, state: torch.Tensor) -> torch.Tensor:
        """
        Compute state value
        
        Args:
            state: State tensor [batch_size, input_dim]
            
        Returns:
            Value tensor [batch_size, 1]
        """
        return self.network(state)


def create_evolution_network(max_gimbal_angle: float = np.pi/6) -> SimpleControlNetwork:
    """Create a network for evolutionary training as described in video"""
    config = NetworkConfig(
        input_dim=3,  # [thrust, angle, angular_velocity]
        output_dim=1,  # [gimbal_angle]
        hidden_layers=[16, 16],  # Small networks for fast evolution
        activation='tanh',
        output_activation='tanh',
        output_scale=max_gimbal_angle  # Scale to max gimbal angle
    )
    return SimpleControlNetwork(config)


