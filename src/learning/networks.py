class SimpleControlNetwork(nn.Module):
    """
    Simple feedforward network for TVC control
    
    As described in the video:
    - Inputs: thrust, angle, angular_velocity (3 inputs)
    - Hidden: 2 small hidden layers 
    - Output: gimbal_angle (1 output)
    """
    
    def __init__(self, config: NetworkConfig):
        super().__init__()
        self.config = config
        
        # Build layers
        layers = []
        prev_dim = config.input_dim
        
        # Hidden layers
        for hidden_dim in config.hidden_layers:
            layers.extend([
                nn.Linear(prev_dim, hidden_dim),
                self._get_activation(config.activation)
            ])
            prev_dim = hidden_dim
            
        # Output layer
        layers.append(nn.Linear(prev_dim, config.output_dim))
        
        # Output activation for bounded control
        if config.output_activation:
            layers.append(self._get_activation(config.output_activation))
            
        self.network = nn.Sequential(*layers)
        
        # Initialize weights
        self._initialize_weights()
        
    def _get_activation(self, activation: str) -> nn.Module:
        """Get activation function by name"""
        activations = {
            'tanh': nn.Tanh(),
            'relu': nn.ReLU(),
            'sigmoid': nn.Sigmoid(),
            'leaky_relu': nn.LeakyReLU(),
        }
        return activations[activation]
    
    def _initialize_weights(self):
        """Initialize network weights with Xavier initialization"""
        for layer in self.network:
            if isinstance(layer, nn.Linear):
                nn.init.xavier_uniform_(layer.weight)
                nn.init.zeros_(layer.bias)
    
    def forward(self, x: torch.Tensor) -> torch.Tensor:
        """
        Forward pass
        
        Args:
            x: Input tensor [batch_size, 3] containing [thrust, angle, angular_velocity]
            
        Returns:
            Output tensor [batch_size, 1] containing [gimbal_angle]
        """
        output = self.network(x)
        # Scale output to control bounds
        return output * self.config.output_scale
    
    def get_weights_as_vector(self) -> np.ndarray:
        """Get all network weights as a flat vector (for evolution)"""
        weights = []
        for param in self.parameters():
            weights.append(param.data.cpu().numpy().flatten())
        return np.concatenate(weights)
    
    def set_weights_from_vector(self, weights: np.ndarray):
        """Set network weights from a flat vector (for evolution)"""
        idx = 0
        for param in self.parameters():
            param_size = param.numel()
            param.data = torch.from_numpy(
                weights[idx:idx+param_size].reshape(param.shape)
            ).float()
            idx += param_size
    
    def copy_weights_from(self, other: 'SimpleControlNetwork'):
        """Copy weights from another network"""
        self.load_state_dict(other.state_dict())
    
    def mutate(self, mutation_strength: float = 0.1):
        """Add Gaussian noise to weights (for evolution)"""
        with torch.no_grad():
            for param in self.parameters():
                noise = torch.randn_like(param) * mutation_strength
                param.add_(noise)
    
    def count_parameters(self) -> int:
        """Count total number of parameters"""
        return sum(p.numel() for p in self.parameters())
    
    def __getstate__(self):
        """Custom serialization for multiprocessing compatibility"""
        # Save only the essential state: config and trained weights
        return {
            'config': self.config,
            'state_dict': self.state_dict()
        }
    
    def __setstate__(self, state):
        """Custom deserialization for multiprocessing compatibility"""
        # Initialize the module first
        super().__init__()
        
        # Restore the config
        self.config = state['config']
        
        # Rebuild the network architecture
        layers = []
        prev_dim = self.config.input_dim
        
        # Hidden layers
