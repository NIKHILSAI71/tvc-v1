    def _simulate_lqr_episode(self, x0: np.ndarray, use_safety: bool = False) -> EpisodeMetrics:
        """Simulate single episode with LQR controller"""
        
        # Initialize metrics collector
        collector = MetricsCollector()
        
        # Simulation parameters
        dt = 0.01
        t_final = 5.0
        time_steps = np.arange(0, t_final, dt)
        
        # Initial state
        self.plant.reset(x0.copy())
        x = self.plant.get_observation(add_noise=False)
        reference = np.array([0.0, 0.0])  # Stabilize to upright
        
        for t in time_steps:
            # Compute control action
            u_lqr = self.lqr_controller.control(x, reference)
            
            # Apply safety filter if requested
            if use_safety:
                filt = self.safety_filter.filter_control(u_lqr, x)
                u = filt['control']
            else:
                u = u_lqr
            
            # Record data
            collector.step(float(t), x, np.array([u], dtype=float), reference)
            
            # Simulate plant dynamics
            x = self.plant.step(u, dt=dt, add_disturbance=False)
            
            # Check termination conditions
            if np.abs(x[0]) > 1.57 or np.abs(x[1]) > 15.0 or not self.plant.is_safe():
                return collector.finalize_episode(success=False, termination_reason="safety_violation")
        
        # Check success criteria
        final_error = np.abs(x[0])  # Final angle error
        success = final_error < 0.1  # Within 0.1 radians of upright
        
        return collector.finalize_episode(success=success)
    
    def _simulate_neural_episode(self, x0: np.ndarray, network: SimpleControlNetwork, 
                                use_safety: bool = False) -> EpisodeMetrics:
        """Simulate single episode with neural network controller"""
        
        # Initialize metrics collector
        collector = MetricsCollector()
        
        # Simulation parameters
        dt = 0.01
        t_final = 5.0
        time_steps = np.arange(0, t_final, dt)
        
        # Initial state
        self.plant.reset(x0.copy())
        x = self.plant.get_observation(add_noise=False)
        reference = np.array([0.0, 0.0])  # Stabilize to upright
        
        for t in time_steps:
            # Compute control action from neural network
            thrust = self.plant.params.nominal_thrust
            state_tensor = torch.tensor([thrust, x[0], x[1]], dtype=torch.float32).unsqueeze(0)
            with torch.no_grad():
                u_neural = network(state_tensor).numpy().flatten()
            
            # Apply safety filter if requested
            if use_safety:
                filt = self.safety_filter.filter_control(float(u_neural[0]), x)
                u = filt['control']
            else:
                u = float(u_neural[0])
            
            # Record data
            collector.step(float(t), x, np.array([u], dtype=float), reference)
            
            # Simulate plant dynamics
            x = self.plant.step(u, dt=dt, add_disturbance=False)
            
            # Check termination conditions
            if np.abs(x[0]) > 1.57 or np.abs(x[1]) > 15.0 or not self.plant.is_safe():
                return collector.finalize_episode(success=False, termination_reason="safety_violation")
        
        # Check success criteria
        final_error = np.abs(x[0])  # Final angle error
        success = final_error < 0.1  # Within 0.1 radians of upright
        
        return collector.finalize_episode(success=success)
    
    def _simulate_ppo_episode(self, x0: np.ndarray, policy,
                             use_safety: bool = False) -> EpisodeMetrics:
        """Simulate single episode with PPO policy"""
        
        # Initialize metrics collector
        collector = MetricsCollector()
        
        # Simulation parameters
        dt = 0.01
        t_final = 5.0
        time_steps = np.arange(0, t_final, dt)
        
        # Initial state
        self.plant.reset(x0.copy())
        x = self.plant.get_observation(add_noise=False)
        reference = np.array([0.0, 0.0])  # Stabilize to upright
        
        for t in time_steps:
            # Compute LQR baseline
            u_lqr = self.lqr_controller.control(x, reference)
            
            # Compute residual from policy
            from tensordict import TensorDict
            obs_td = TensorDict({"observation": torch.tensor(x, dtype=torch.float32)}, batch_size=())
            with torch.no_grad():
                act_td = policy(obs_td)
            u_residual = act_td["action"].cpu().numpy().flatten()
            
            # Total control action
            u_total = float(u_lqr) + float(u_residual[0])
            
            # Apply safety filter if requested
            if use_safety:
                filt = self.safety_filter.filter_control(u_total, x)
                u = filt['control']
            else:
                u = u_total
            
            # Record data
            collector.step(float(t), x, np.array([u], dtype=float), reference)
            
            # Simulate plant dynamics
            x = self.plant.step(u, dt=dt, add_disturbance=False)
            
            # Check termination conditions
            if np.abs(x[0]) > 1.57 or np.abs(x[1]) > 15.0 or not self.plant.is_safe():
                return collector.finalize_episode(success=False, termination_reason="safety_violation")
        
        # Check success criteria
        final_error = np.abs(x[0])  # Final angle error
        success = final_error < 0.1  # Within 0.1 radians of upright
        
        return collector.finalize_episode(success=success)
    
