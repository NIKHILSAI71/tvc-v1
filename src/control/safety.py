"""
Control Barrier Function (CBF) and Control Lyapunov Function (CLF) 
Quadratic Programming (QP) Safety Filter

This module implements the safety-critical control framework that guarantees:
1. Forward invariance of safe sets through CBF constraints
2. Stability through CLF constraints (soft)
3. Real-time QP solving with OSQP

Mathematical formulation:
minimize_{u,δ} (1/2)||u - u_des||² + ρδ²
subject to:
  - CLF: L_f V + L_g V * u + γV ≤ δ  (soft stability)
  - CBF: L_f h + L_g h * u ≥ -α(h)    (hard safety)
  - Actuator: u_min ≤ u ≤ u_max         (hard limits)

Author: Generated by GitHub Copilot
"""

