def train_evolution(config_path: Optional[str] = None, save_model: bool = True):
    """Train neuro-evolutionary controller"""
    print("="*60)
    print("TRAINING NEURO-EVOLUTIONARY TVC CONTROLLER")
    print("="*60)
    
    # Load configuration
    if config_path:
        try:
            config_dict = load_yaml_config(config_path)
            config = create_config_object(config_dict)
            print(f"Using configuration: {getattr(config, 'experiment_name', 'unnamed_experiment')}")
        except Exception as e:
            print(f"Error loading configuration: {e}")
            print("Falling back to default configuration...")
            config_dict = get_default_config()
            config_dict['experiment_name'] = "evolution_training"
            config = create_config_object(config_dict)
    else:
        config_dict = get_default_config()
        config_dict['experiment_name'] = "evolution_training"
        config = create_config_object(config_dict)
    print("Using default configuration")
    
    # Convert config to parameter objects
    plant_params = config_to_plant_params(config)
    evo_params = config_to_evolution_params(config)
    
    # Initialize components
    use_safety = bool(getattr(config, 'use_safety_filter', True))
    trainer = EvolutionaryTrainer(plant_params, evo_params, use_safety)
    
    print(f"Population size: {evo_params.population_size}")
    print(f"Max generations: {evo_params.max_generations}")
    print(f"Simulation duration: {evo_params.sim_duration}s")
    print(f"Safety filter: {'enabled' if use_safety else 'disabled'}")
    
    # Train
    start_time = time.time()
    results = trainer.train()
    training_time = time.time() - start_time
    
    print(f"\nTraining completed in {training_time:.2f} seconds")
    print(f"Best fitness: {results['best_fitness']:.6f}")
    print(f"Convergence generation: {results['generations']}")
    
    # Extract results
    best_network = results['best_network']
    training_curves = results['fitness_history']
    
    # Save results
    output_dir = Path(getattr(config, 'output_dir', 'results'))
    output_dir.mkdir(parents=True, exist_ok=True)
    
    if save_model:
        import torch
        model_path = output_dir / "evolution_controller.pth"
        torch.save(best_network.state_dict(), model_path)
        print(f"Model saved to {model_path}")
    
    # Save training curves
    import json
    import numpy as np
    
    # Convert numpy arrays to lists for JSON serialization
    curves_for_json = {}
    for key, value in training_curves.items():
        if isinstance(value, np.ndarray):
            curves_for_json[key] = value.tolist()
        elif isinstance(value, list):
            curves_for_json[key] = [float(x) if isinstance(x, (np.floating, np.integer)) else x for x in value]
        else:
            curves_for_json[key] = value
    
    curves_path = output_dir / "evolution_training_curves.json"
    with open(curves_path, 'w') as f:
        json.dump(curves_for_json, f, indent=2)
    print(f"Training curves saved to {curves_path}")
    
    return best_network, training_curves


