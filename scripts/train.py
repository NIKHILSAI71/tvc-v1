# Simple config loading utility
def load_yaml_config(config_path: str) -> Dict[str, Any]:
    """Load YAML configuration file with error handling"""
    try:
        config_file = Path(config_path)
        if not config_file.exists():
            # Try relative path from script location
            script_dir = Path(__file__).parent
            relative_config = script_dir / config_path
            if relative_config.exists():
                config_file = relative_config
            else:
                # Try looking in configs directory
                configs_config = script_dir.parent / "configs" / Path(config_path).name
                if configs_config.exists():
                    config_file = configs_config
                else:
                    raise FileNotFoundError(f"Configuration file not found: {config_path}")
        
        with open(config_file, 'r') as f:
            config = yaml.safe_load(f)
            
        if not isinstance(config, dict):
            raise ValueError(f"Configuration file must contain a dictionary, got {type(config)}")
            
        print(f"Successfully loaded configuration from: {config_file}")
        return config
        
    except yaml.YAMLError as e:
        raise ValueError(f"Invalid YAML syntax in {config_path}: {e}")
    except Exception as e:
        raise RuntimeError(f"Failed to load configuration from {config_path}: {e}")

